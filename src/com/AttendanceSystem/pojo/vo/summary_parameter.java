package com.AttendanceSystem.pojo.vo;

public class summary_parameter {

	private String User_ID;
	
	private String Affiliation_name;
	
	private String WorkTime;
	
	private String OffworkTime;
	
	private String Vacation_Type;
	
	private String date;
	
	private String Remark;

	public String getUser_ID() {
		return User_ID;
	}

	public void setUser_ID(String user_ID) {
		User_ID = user_ID;
	}

	public String getAffiliation_name() {
		return Affiliation_name;
	}

	public void setAffiliation_name(String affiliation_name) {
		Affiliation_name = affiliation_name;
	}

	public String getWorkTime() {
		return WorkTime;
	}

	public void setWorkTime(String workTime) {
		WorkTime = workTime;
	}

	public String getOffworkTime() {
		return OffworkTime;
	}

	public void setOffworkTime(String offworkTime) {
		OffworkTime = offworkTime;
	}

	public String getVacation_Type() {
		return Vacation_Type;
	}

	public void setVacation_Type(String vacation_Type) {
		Vacation_Type = vacation_Type;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getRemark() {
		return Remark;
	}

	public void setRemark(String remark) {
		Remark = remark;
	}
	
	
}
